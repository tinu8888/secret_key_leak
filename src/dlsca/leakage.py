# Precomputed popcount table for bytes 0-255 -> Hamming weight 0-8.
_HW_TABLE = np.array([bin(i).count("1") for i in range(256)], dtype=np.uint8)


def sbox(x):
    """AES forward S-box applied element-wise.

    Args:
        x: int or array-like of byte values (0-255). Values are taken mod 256.

    Returns:
        ``uint8`` scalar or array of S-box outputs, matching the input shape.
    """
    arr = np.asarray(x, dtype=np.int64) & 0xFF
    out = SBOX[arr]
    if np.ndim(x) == 0:
        return int(out)
    return out


def hamming_weight(x):
    """Hamming weight (number of set bits, 0-8) of each byte, the CPA leakage model.

    Args:
        x: int or array-like of byte values (0-255).

    Returns:
        ``uint8`` scalar or array of Hamming weights in [0, 8].
    """
    arr = np.asarray(x, dtype=np.int64) & 0xFF
    out = _HW_TABLE[arr]
    if np.ndim(x) == 0:
        return int(out)
    return out


def identity_label(x):
    """Identity label (the byte value itself, 0-255), the CNN 256-class label.

    Args:
        x: int or array-like of byte values (0-255).

    Returns:
        ``uint8`` scalar or array equal to the input mod 256.
    """
    arr = np.asarray(x, dtype=np.int64) & 0xFF
    out = arr.astype(np.uint8)
    if np.ndim(x) == 0:
        return int(out)
    return out


def intermediate(plaintext, key, byte):
    """First-round S-box intermediate ``Sbox(plaintext[byte] xor key[byte])``.

    Accepts either single 16-byte vectors or batched ``(N, 16)`` arrays for plaintext/key,
    and ``key`` may be a single candidate byte (int) when sweeping key guesses in CPA/CNN.

    Args:
        plaintext: 16-length vector or ``(N, 16)`` array of plaintext bytes.
        key: 16-length vector, ``(N, 16)`` array, or a single int key-byte candidate.
        byte: which key byte index (0-15) is attacked.

    Returns:
        ``uint8`` scalar or 1-D array (length N) of S-box outputs for the attacked byte.
    """
    pt = np.asarray(plaintext, dtype=np.int64) & 0xFF
    if byte < 0 or byte > 15:
        raise ValueError(f"byte index must be in 0..15, got {byte}")

    if pt.ndim == 1:
        p_b = pt[byte]
        scalar_pt = True
    elif pt.ndim == 2:
        p_b = pt[:, byte]
        scalar_pt = False
    else:
        raise ValueError(f"plaintext must be 1-D (16,) or 2-D (N,16), got shape {pt.shape}")

    if np.ndim(key) == 0:
        k_b = int(key) & 0xFF
    else:
        k = np.asarray(key, dtype=np.int64) & 0xFF
        if k.ndim == 1:
            k_b = k[byte]
        elif k.ndim == 2:
            k_b = k[:, byte]
        else:
            raise ValueError(f"key must be int, (16,), or (N,16); got shape {k.shape}")

    return sbox(p_b ^ k_b)
